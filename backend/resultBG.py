def messageBG(arr, word):
    result = "Има "
    objects = []
    if word == "all":
        if len(arr) == 0:
            return "Няма намерени обекти"
        for det in arr:
            if det[0] in objects:
                continue
            objects.append(det[0])
            br = count(arr, det[0])
            result += str(br) + " "
            if br>1:
                result += plural[det[0]]
            else:
                result += translate[det[0]]
            result += ", "
    else:
        if len(arr) == 0:
            return "Няма намерени " + plural[word]
        br = count(arr, arr[0][0])
        if br>1:
            result += str(br) + " " + plural[arr[0][0]] + " пред телефона. "
            for det in arr:
                result += one[word]+" "
